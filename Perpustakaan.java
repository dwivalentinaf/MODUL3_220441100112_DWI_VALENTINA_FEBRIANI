    private void cbPilihanBukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbPilihanBukuActionPerformed
        // TODO add your handling code here:
        String selectedBuku = (String) cbPilihanBuku.getSelectedItem();

        switch (selectedBuku) {
            case "Novel Ayah":
                txtJudulBuku.setText("Judul Buku : Ayah");
                txtNamaPengarang.setText("Nama Pengarang : Andrea Hirata");
                txtTahunTerbit.setText("Tahun Terbit : 2009 ");
                txtDeskripsi.setText(" Novel ini berkisah tentang perjuangan seorang ayah ");
                break;
            case "Novel Negeri 5 Menara":
                txtJudulBuku.setText("Judul Buku : Negeri 5 Menara ");
                txtNamaPengarang.setText("Nama Pengarang : A.Fuadi ");
                txtTahunTerbit.setText("Tahun Terbit : 2009 ");
                txtDeskripsi.setText(" Negeri 5 Menara mengisahkan kisah lima pemuda yang "
                        + "berasal dari berbagai daerah di Indonesia dalam berjuang meraih mimpi.");
                break;
            case "Novel Sang Pemimpi":
                txtJudulBuku.setText("Judul Buku : Sang Pemimpi");
                txtNamaPengarang.setText("Nama Pengarang : Andrea Hirata");
                txtTahunTerbit.setText("Tahun Terbit : 2006 ");
                txtDeskripsi.setText(" Novel ini menceritakan mimpi besar seorang anak untuk mendirikan Sekolah di Desa ");
                break;
            case "Novel Edensor" :
                txtJudulBuku.setText("Judul Buku : Edensor");
                txtNamaPengarang.setText("Nama Pengarang : Andrea Hirata");
                txtTahunTerbit.setText("Tahun Terbit : 2007 ");
                txtDeskripsi.setText(" Novel Edensor merupakan rekaan kisah nyata yang dialami oleh penulis ");
                break;
            default:
                // Handle the case where "Pilih Buku" or unknown item is selected
                txtJudulBuku.setText("");
                txtNamaPengarang.setText("");
                txtTahunTerbit.setText("");
                txtDeskripsi.setText("");
                break;
        }
    }//GEN-LAST:event_cbPilihanBukuActionPerformed

