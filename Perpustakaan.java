    private void UbahBackgroundColor() {
        // untuk mengambil indeks terpilih dari combo box
        int selectedIndex = cbPilihanBuku.getSelectedIndex();

   
        switch (selectedIndex) {
            case 0:
                txtJudulBuku.setBackground(Color.WHITE); 
                txtNamaPengarang.setBackground(Color.WHITE); 
                txtTahunTerbit.setBackground(Color.WHITE); 
                txtDeskripsi.setBackground(Color.WHITE); 
                break;
            case 1:
                txtJudulBuku.setBackground(new Color(255, 182, 193)); // Light Pink
                txtNamaPengarang.setBackground(Color.WHITE); // White
                txtTahunTerbit.setBackground(new Color(211, 211, 211)); // Light grey
                txtDeskripsi.setBackground(new Color(173, 216, 230)); // Light Blue
                break;
            case 2:
                txtJudulBuku.setBackground(Color.YELLOW);
                txtNamaPengarang.setBackground(new Color(173, 216, 230)); // Light Blue
                txtTahunTerbit.setBackground(new Color(0, 105, 148)); // Oren
                txtDeskripsi.setBackground(new Color(192, 192, 192)); // Light grey
                break;
            case 3:
                txtJudulBuku.setBackground(new Color(255, 182, 193));
                txtNamaPengarang.setBackground(new Color(221, 160, 221)); // Lavender
                txtTahunTerbit.setBackground(new Color(173, 216, 230)); // Light Blue
                txtDeskripsi.setBackground(Color.CYAN); // cyan
                break;
            case 4:
                // Novel Edensor dipilih, ubah warna latar belakang sesuai keinginan
                txtJudulBuku.setBackground(new Color(255, 182, 193)); // Light Pink
                txtNamaPengarang.setBackground(Color.WHITE); // White
                txtTahunTerbit.setBackground(new Color(211, 211, 211)); // Light grey
                txtDeskripsi.setBackground(new Color(173, 216, 230)); // Light Blue
                break;
            default:
                txtJudulBuku.setBackground(getContentPane().getBackground());
                break;
        }
    }

